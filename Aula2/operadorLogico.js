//Comparadores Lógicos

//Operador Lógico 'E' &&
//operador Lógico 'OU' ||
//Operador Lógico 'Não' ou 'Negação' !

const valor1 = true
const valor2 = false
const valor3 = true

//Verifica se o valor1 E o valor2 são true
//Se um deles for false, o resultado é false
console.log(valor1 && valor2 && valor3) //false

//Verifica se o valor1 OU o valor2 são true
//Se um deles for true, o resultado é true
console.log(valor1 || valor2 || valor3)//true

valor1 //true
!valor1 //false